<p>
	p4.turtdur.us is a simple web app where you can search for any movie, view movies currently in theaters, 
	and view movies on DVD.  For any movie, you can view detailed movie info from the RottenTomatoes API,
	write a review, and view the reviews of other users. Use the menu to the left or the search bar above to
	get started.  To write a review, you must sign up for an account and log in.
</p>